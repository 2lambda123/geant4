#ifndef Tst17CalorHit_h
#define Tst17CalorHit_h 1

#include "G4VHit.hh"
#include "G4THitsCollection.hh"
#include "G4Allocator.hh"

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo....

class Tst17CalorHit : public G4VHit
{
  public:

      Tst17CalorHit();
     ~Tst17CalorHit();
      Tst17CalorHit(const Tst17CalorHit&);
      const Tst17CalorHit& operator=(const Tst17CalorHit&);
      int operator==(const Tst17CalorHit&) const;

      inline void* operator new(size_t);
      inline void  operator delete(void*);

      void Print();
      
  public:
  
      void AddAbs(G4double de, G4double dl) {EdepAbs += de; TrackLengthAbs += dl;};
      void AddGap(G4double de, G4double dl) {EdepGap += de; TrackLengthGap += dl;};      
                 
      G4double GetEdepAbs()     { return EdepAbs; };
      G4double GetTrakAbs()     { return TrackLengthAbs; };
      G4double GetEdepGap()     { return EdepGap; };
      G4double GetTrakGap()     { return TrackLengthGap; };
     
  private:
  
      G4double EdepAbs, TrackLengthAbs;
      G4double EdepGap, TrackLengthGap;
      
};

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo....

typedef G4THitsCollection<Tst17CalorHit> Tst17CalorHitsCollection;

extern G4Allocator<Tst17CalorHit> Tst17CalorHitAllocator;

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo....

inline void* Tst17CalorHit::operator new(size_t)
{
  void* aHit;
  aHit = (void*) Tst17CalorHitAllocator.MallocSingle();
  return aHit;
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo....

inline void Tst17CalorHit::operator delete(void* aHit)
{
  Tst17CalorHitAllocator.FreeSingle((Tst17CalorHit*) aHit);
}

#endif


