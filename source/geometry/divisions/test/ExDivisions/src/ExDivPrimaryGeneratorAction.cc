#include "ExDivPrimaryGeneratorAction.hh"
#include "ExDivDetectorConstruction.hh"

#include "G4Event.hh"
#include "G4ParticleGun.hh"
#include "G4ParticleTable.hh"
#include "G4ParticleDefinition.hh"
#include "globals.hh"

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

ExDivPrimaryGeneratorAction::
ExDivPrimaryGeneratorAction(ExDivDetectorConstruction* myDC)
  : myDetector(myDC)
{
  G4int n_particle = 1;
  particleGun = new G4ParticleGun(n_particle);

// default particle

  G4ParticleTable* particleTable = G4ParticleTable::GetParticleTable();
  G4ParticleDefinition* particle = particleTable->FindParticle("geantino");
  
  particleGun->SetParticleDefinition(particle);
  particleGun->SetParticleMomentumDirection(G4ThreeVector(0.,0.,1.));
  particleGun->SetParticleEnergy(3.0*GeV);
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

ExDivPrimaryGeneratorAction::~ExDivPrimaryGeneratorAction()
{
  delete particleGun;
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void ExDivPrimaryGeneratorAction::GeneratePrimaries(G4Event* anEvent)
{ 
  G4double position = -myDetector->GetWorldLengthZ();
  particleGun->SetParticlePosition(G4ThreeVector(0.,0.,position));
  
  particleGun->GeneratePrimaryVertex(anEvent);
}

