      while (lE>Xj && lastL<nL)            // lE is X in this case
      {
        lastL++;
        Xp=Xj;
        Xj=X[lastL];
      }
      G4double YNj=lE*lastPhi[lastL]-lastFun[lastL];
      G4double YN1=lE*lastPhi[lastL-1]-lastFun[lastL-1];
