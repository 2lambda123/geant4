#include "MLRunManager.hh"
////////////////////////////////////////////////////////////////////////////////
//
MLRunManager::MLRunManager ()
  :timeused(0.)
{
  timer = new G4Timer();
  timer->Start();
}
////////////////////////////////////////////////////////////////////////////////
//
MLRunManager::~MLRunManager ()
{
  delete timer;
  G4cout <<"Run Manager deleting... " <<G4endl;
}
////////////////////////////////////////////////////////////////////////////////
//
G4double MLRunManager::GetTimeUsed ()
{
  timer->Stop();
  timeused += (timer->GetUserElapsed() + timer->GetSystemElapsed() );
  timer->Start();
  return timeused;
}
