G4int G4ASTARStopping:: GetIndex (const G4Material* mat)
{  
  if (mat == currentMaterial) return matIndex;
  G4String matName = mat->GetName();
  for (G4int i=0; i<74; i++){
    if (matName == name[i]){
      matIndex= -1;
      currentMaterial = mat;
      return i;
    }
  }
  return -1;
}

