void Em0PhysicsList::SetGammaCut(G4double val)
{

  // reset cut value tables for all particles
  ResetCuts();

  cutForGamma = val;
}

void Em0PhysicsList::SetECut(G4double val)
{
  // reset cut value tables for all particles
  ResetCuts();

  cutForE = val;
}


void Em0PhysicsList::SetCutsByEnergy(G4double val)
{
  G4ParticleTable* theParticleTable =  G4ParticleTable::GetParticleTable();
  G4Material* currMat = pDet->GetMaterial();

  G4UImanager* UI = G4UImanager::GetUIpointer();
  
  // set cut values for gamma at first and for e- second and next for e+,
  // because some processes for e+/e- need cut values for gamma 
  G4ParticleDefinition* part;
  G4double cut;
  UI->ApplyCommand("/run/particle/dumpCutValues");
  part = theParticleTable->FindParticle("e-");
  cut = G4EnergyLossTables::GetRange(part,val,currMat);
  SetCutValue(cut, "e-");
  UI->ApplyCommand("/run/particle/dumpCutValues");
  part = theParticleTable->FindParticle("e+");
  cut = G4EnergyLossTables::GetRange(part,val,currMat);
  SetCutValue(cut, "e+");
  UI->ApplyCommand("/run/particle/dumpCutValues");
  
  // set cut values for proton and anti_proton before all other hadrons
  // because some processes for hadrons need cut values for proton/anti_proton 
  part = theParticleTable->FindParticle("proton");
  cut = G4EnergyLossTables::GetRange(part,val,currMat);
  SetCutValue(cut, "proton");
  UI->ApplyCommand("/run/particle/dumpCutValues");
  part = theParticleTable->FindParticle("anti_proton");
  cut = G4EnergyLossTables::GetRange(part,val,currMat);
  SetCutValue(cut, "anti_proton");
  UI->ApplyCommand("/run/particle/dumpCutValues");
   
  SetCutValueForOthers(cut);
  UI->ApplyCommand("/run/particle/dumpCutValues");
}

void Em0PhysicsList::GetRange(G4double val)
{
  G4ParticleTable* theParticleTable =  G4ParticleTable::GetParticleTable();
  G4Material* currMat = pDet->GetMaterial();

  G4ParticleDefinition* part;
  G4double cut;
  part = theParticleTable->FindParticle("e-");
  cut = G4EnergyLossTables::GetRange(part,val,currMat);
  G4cout << "material : " << currMat->GetName() << G4endl;
  G4cout << "particle : " << part->GetParticleName() << G4endl;
  G4cout << "energy   : " << val / keV << " (keV)" << G4endl;
  G4cout << "range    : " << cut / mm << " (mm)" << G4endl;
}











